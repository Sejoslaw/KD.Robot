namespace KD.Robot.WinApi
{
    /// <summary>
    /// Indicates the specified Mouse Data Flags.
    /// </summary>
    enum MouseDataFlags : uint
    {
        XBUTTON1 = 0x0001, // Set if the first X button was pressed or released.
        XBUTTON2 = 0x0002 // Set if the second X button was pressed or released.
    }
}